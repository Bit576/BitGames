function showFullScreenSquare(icon, description, c1, c2, img, href) {
    const centerText = document.getElementById("centerText");
    centerText.style.opacity = 0; // Скрываем центральный текст
    
    const fullScreenSquare = document.getElementById("fullScreenSquare");
    fullScreenSquare.style.display = "block"; // Показать блок
    fullScreenSquare.style.opacity = 1; // Сделать его видимым

    const button = document.getElementById("instalButton");
    const rightRectangle = document.getElementById("rightRectangle");
    const textBlock = document.querySelector(".textInsideSquare");
    const imageInsideSquare = document.querySelector(".imageInsideSquare");
    const allAdobe = document.querySelector(".top-right-text");

    // Убираем элементы (они исчезнут плавно)
    rightRectangle.style.transition = "opacity 0.2s ease-out";
    imageInsideSquare.style.transition = "opacity 0.2s ease-out";
    button.style.transition = "opacity 0.2s ease-out";
    textBlock.style.transition = "opacity 0.2s ease-out";

    rightRectangle.style.opacity = 0;
    imageInsideSquare.style.opacity = 0;
    button.style.opacity = 0;
    textBlock.style.opacity = 0;

    setTimeout(() => {
        button.style.color = c1;
        button.style.backgroundColor = c2;
        rightRectangle.src = img;
        textBlock.innerHTML = description;  // Заменил textContent на innerHTML
        imageInsideSquare.src = icon;
        button.style.setProperty('--button-color', c1);
        allAdobe.style.setProperty('--button-color', c1);
        allAdobe.style.backgroundColor = c2;
        allAdobe.style.color = c1;

    
        setTimeout(() => {
            rightRectangle.style.opacity = 1;
            imageInsideSquare.style.opacity = 1;
            button.style.opacity = 1;
            textBlock.style.opacity = 1;
            button.onclick= function() { window.open(href, "_blank"); };
        }, 100);
    }, 200);
}
